;chain of light

	ORG 2000H

	MVI A,80H
	OUT 83H
	LXI SP,2FFFH

loop1:	LXI H,3000H
	MVI D,01H
loop:	MOV A,M
	OUT 80H
	MOV A,D
	CALL 2050H
	INX H
	INR D
	CPI 10H
	JZ loop1
	JMP loop
	HLT
	ORG 3000H
	DB 01H,02H,04H,08H,10H,20H,40H,80H,80H,40H,20H,10H,08H,04H,02H,01H

	ORG 2050H
	MVI B,01H
L3:	MVI C,04H
L2:	DCR C
	JNZ L2
	DCR B
	JNZ L3
	RET


